							<p>如何快捷申请父母，配偶及子女移民加拿大？<br />
担保者未符标准如何合法申请？报税与申请团聚移民有何联系？<br />
如何协助同性或同居伴侣申请移民？</p>

<p>************************************************************************<br />
<strong>加国家庭团聚移民概述及申请要点 </strong><br />
所有的加拿大公民或加拿大的永久居民（简称“担保人”），只要年满19岁以上，均可申请担保以下的亲属移民加拿大，包括：<br />
　　1、配偶或同居伴侣（16岁以上）；<br />
　　2、父母亲或祖父母；<br />
　　3、22岁以下的子女或虽然超过22岁，但仍在校全职读书的子女（“子女”包括亲生和18岁以下的收养子女）<br />
　　4、18岁以下，孤儿身份的兄弟，姐妹及堂兄弟，姐妹；必须是未婚及未有同居伴侣者；<br />
　　5、如果担保人是单身一人在加拿大，且也无以上所述任何亲属可以担保，则他或她的任意一位亲属。</p>
<p>　  原则上来说，担保人必须居住在加拿大。这一居住是指物理意义上的居住，即本人必须亲身在加拿大。但是，这一规定不适用以下情况：<br />

　　1. 如果担保人在国外的原因是旅游或短期探访亲友；或者，<br />
　　2. 如果担保人本人是加拿大公民，但长期居住在国外，而现在被担保的人是该公民的配偶或其子女，而且，一但被担保人获得加拿大永久居民身份，该公民将与他的配偶或子女一起返回加拿大居住。<br />
　　法律还规定，如果担保人的收入不够，就不可以担保他的亲属移民加拿大。1997年所修订的法律规定，担保人的收入必须来自加拿大，而不能是其它任何地方。这些收入可以包括工资，利息、红利、租金以及自雇人士的任何收入。收入的总额要求取决于家庭人数（详见附表）。如果是担保自己的配偶或子女，可以不必满足最低的收入要求。</p>
<p><strong>申请家庭团聚移民的最新程序</strong><br />
  自2003年2月17日起，加拿大公民暨移民部（CIC）开始新的家庭团聚类移民申请审核程序:为海外申请家庭团聚类移民中的配偶、同居伴侣及子女申请启用一套新的申请表。在申请中将担保申请、移民申请及移民体检三个过程同步进行。新办法的最大改变是允许申请人在递交表格前先进行体检，从而大幅减少个案处理时间。</p>
<p>   新办法主要针对加国公民或永久居民在海外的配偶、同居伴侣(COMMON-LAW PARTNERS)、未成年子女申请加国永久居民的个案。已经居住在加国的配偶或子女的申请个案，并不受新办法的影响。</p>
<p>　 2002年六月二十八日联邦移民部颁布的一项新法案，将原本分开进行的担保人资格审查(SPONSORSHIP APPLICATION)和被担保人申请永久居民(PERMANENT RESIDENT APPLICATION)的审查合二为一，只需填写一套联合申请表格呈交到位于密西沙加的移民部「担保依亲移民个案处理中心」。 三步同跨加快了申请速度，是指在加拿大的担保人在将其中的担保表格填好并寄给移民部的同时，将其余的体检表及移民申请表寄给海外的配偶及子女。移民部强调，允许申请者完成体检先于移民申请表的递交。</p>

		
		
