						<h2>住家保姆工作签证</h2>
							<p>“住家保姆或护理人员移民”项目是加拿大移民部最近实行的一项移民计划，目的是填补加拿大境内的劳动力市场对这一工作职位的短缺。在此计划下，申请者可申请来加国从事保姆或住家护理员工作签证。工作满两年之后，即可申请移民加拿大，获得永久居民的资格。</p>
<p><span style="text-decoration: underline;"><strong>海外申请人必须具备如下申请条件：</strong></span></p>
<p><strong>第一，	相当于加拿大高中毕业以上的学历；</strong></p>

<p><strong>第二，申请人曾接受6个月的全日在校护理或保姆培训：</strong>这种培训并不局限于家庭护理，只要相关即可，例如，护士培训、或幼师、幼教培训亦可，但必须获得证书；<br />
或者<br />
申请之前有12个月从事带薪全职护理工作经验：这12个月的带薪护理工作必须在申请前的三年内获得，且必须至少连续六个月受雇于同一个雇主，上述两个条件，申请人必须具备其一。</p>
<p>在一些机构如日托中心，医院，敬老院的护理经验可计为经验。该经验必须与申请人在加国准备申请的工作相关。需要注意的是：函授课程不属于在校培训，但可列为“经验”的一部份。</p>
<p><strong>第三，有一定的读、听、说英语或法语的能力，能够用英文独立应付一些紧急情况。</strong></p>
<p>申请该项计划与一般类别的移民不同的是：申请人必须先在加拿大境内找到合格的雇主，即愿意雇用申请人作为保姆或住家护理员的家庭或个人。</p>
