<p><strong>曼省商业移民的申请条件：</strong></p>
<p>●	必须具有至少三年的拥有并管理企业的经验，或在一家成功的企业里拥有至少三年的高级管理经验。<br />
●	申请人必须拥有至少25万加币的净资产。<br />
●	申请人需要在递交MPNP-B申请之前到曼省进行为期一周以上的商务考察。<br />
●	申请人必须提供详细的商业计划书。<br />
●	申请人得到曼省的提名后，必须与曼省签订“定金协议书”，并交纳75000加币的定金。<br />
<strong><br />
曼尼托巴省提名商业移民的申请费用：</strong></p>

<p>●	曼省的商务考察费用（包括短期签证费和曼省面谈费50加元）。<br />
●	申请MPNP-B无申请费用。<br />
●	联邦移民局的申请费用：主申请人550加元，配偶550加元，每个子女550加元（22岁及以上）或150加元（22岁以下）。<br />
●	落地费用：主申请人975加元，配偶975加元。</p>
